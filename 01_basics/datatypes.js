//HOW TO FIND THE DATATYPE OF ANY GIVEN VARIABLE 
console.log(typeof "Hello World");   // returns string
console.log(typeof 456);             // returns number
console.log(typeof true);           // returns boolean
console.log(typeof null);            // returns object (important)
console.log(typeof undefined);       // returns undefined
console.log(typeof Symbol());        // returns symbol
console.log(BigInt(987654321987654321)); //returns bigint